package assignments_5;

public class DefaultInitialization {
    int class_var_1;
    char class_var_2;

    void printClassVariable() {
        System.out.println(class_var_1);
        System.out.println(class_var_2);
    }

    void printLocalVariable(int local_var_1, char local_var_2) {
        System.out.println(local_var_1);
        System.out.println(local_var_2);
    }
}
